var ciudadAudio1 = document.getElementById('ciudadAudio1');
var ciudadPlayPauseBTN1 = document.getElementById('ciudadPlayPauseBTN1');
var ciudadCount1 = 0;

function ciudadPlayPause1(){
	if(ciudadCount1  == 0){
		ciudadCount1  = 1;
		ciudadAudio1.play();
		ciudadPlayPauseBTN1.innerHTML = "||";
	}else{
		ciudadCount1 = 0;
		ciudadAudio1.pause();
		ciudadPlayPauseBTN1.innerHTML = "►";
	}
}
 
function ciudadStop1(){
	ciudadPlayPause1()
	ciudadAudio1.pause();
	ciudadAudio1.currentTime = 0;
	ciudadPlayPauseBTN1.innerHTML = "►";
}

