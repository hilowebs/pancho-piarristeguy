var turderaAudio1 = document.getElementById('turderaAudio1');
var turderaPlayPauseBTN1 = document.getElementById('turderaPlayPauseBTN1');
var turderaCount1 = 0;

function turderaPlayPause1(){
	if(turderaCount1  == 0){
		turderaCount1  = 1;
		turderaAudio1.play();
		turderaPlayPauseBTN1.innerHTML = "||";
	}else{
		turderaCount1 = 0;
		turderaAudio1.pause();
		turderaPlayPauseBTN1.innerHTML = "►";
	}
}
 
function turderaStop1(){
	turderaPlayPause1()
	turderaAudio1.pause();
	turderaAudio1.currentTime = 0;
	turderaPlayPauseBTN1.innerHTML = "►";
}

