var poniAudio1 = document.getElementById('poniAudio1');
var poniPlayPauseBTN1 = document.getElementById('poniPlayPauseBTN1');
var poniCount1 = 0;

function poniPlayPause1(){
	if(poniCount1  == 0){
		poniCount1  = 1;
		poniAudio1.play();
		poniPlayPauseBTN1.innerHTML = "||";
	}else{
		poniCount1 = 0;
		poniAudio1.pause();
		poniPlayPauseBTN1.innerHTML = "►";
	}
}
 
function poniStop1(){
	poniPlayPause1()
	poniAudio1.pause();
	poniAudio1.currentTime = 0;
	poniPlayPauseBTN1.innerHTML = "►";
}

