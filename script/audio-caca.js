var cacaAudio1 = document.getElementById('cacaAudio1');
var cacaPlayPauseBTN1 = document.getElementById('cacaPlayPauseBTN1');
var cacaCount1 = 0;

function cacaPlayPause1(){
	if(cacaCount1  == 0){
		cacaCount1  = 1;
		cacaAudio1.play();
		cacaPlayPauseBTN1.innerHTML = "||";
	}else{
		cacaCount1 = 0;
		cacaAudio1.pause();
		cacaPlayPauseBTN1.innerHTML = "►";
	}
}
 
function cacaStop1(){
	cacaPlayPause1()
	cacaAudio1.pause();
	cacaAudio1.currentTime = 0;
	cacaPlayPauseBTN1.innerHTML = "►";
}

